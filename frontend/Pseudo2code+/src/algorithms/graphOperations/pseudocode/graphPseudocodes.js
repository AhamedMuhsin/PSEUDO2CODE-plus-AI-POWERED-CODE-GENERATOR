  "  visited ← empty set",
  "  cycleFound ← false",
  "  ",
  "  for each node in graph:",
  "    if node not in visited:",
  "      if DFSVisit(node, null):",
  "        cycleFound ← true",
  "        break",
  "  ",
  "  DFSVisit(node, parent):",
  "    visited.add(node)",
  "    for each neighbor of node:",
  "      if neighbor not in visited:",
  "        DFSVisit(neighbor, node)",
